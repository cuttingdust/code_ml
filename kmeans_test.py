# 导包
from sklearn.cluster import KMeans
from sklearn.datasets import make_blobs
import matplotlib.pyplot as plt
import matplotlib
matplotlib.use('TkAgg')

# TODO 准备数据
# 1.使用 make_blobs 生成人工的“簇状”数据，常用于演示和测试聚类算法。
#
# 返回值：
# x：生成的特征数据，形状为 (1000, 2)。
#    每一行代表一个样本点，每一列代表一个特征；这里可以把两列理解成平面上的 x、y 坐标。
# y：每个样本真实所属的簇编号，形状为 (1000,)，取值通常为 0、1、2、3。
#    y 是 make_blobs 为方便验证结果而提供的“参考答案”。KMeans 属于无监督学习，训练时只使用 x，
#    不会把 y 传给模型；真实的聚类任务通常也没有这样的标签。
x, y = make_blobs(
    # 一共生成 1000 个样本点，这些点会被分配到下面指定的 4 个簇中。
    n_samples=1000,

    # 每个样本有 2 个特征，因此每个簇中心也必须由 2 个坐标值组成。
    # 设置为 2 还方便使用二维散点图观察数据分布。
    n_features=2,

    # 指定各个簇的中心坐标。这里有 4 个中心，因此会生成 4 簇数据：
    # 第 0 簇中心为 [-1, -1]，第 1 簇中心为 [0, 0]，
    # 第 2 簇中心为 [1, 1]，第 3 簇中心为 [2, 2]。
    centers=[[-1, -1], [0, 0], [1, 1], [2, 2]],

    # 设置每个簇的标准差，也就是样本点围绕中心的分散程度。
    # 数值越小，样本越靠近簇中心；数值越大，样本分布得越分散。
    # 4 个数依次对应上面的 4 个中心：
    # [-1, -1] 周围最分散，标准差为 0.4；
    # [0, 0] 和 [1, 1] 周围更集中，标准差均为 0.2；
    # [2, 2] 周围的分散程度居中，标准差为 0.3。
    cluster_std=[0.4, 0.2, 0.2, 0.3],

    # 固定随机种子，使每次运行时生成的数据都相同，方便复现实验结果。
    # 改成其他整数会生成另一组数据；不设置时，每次运行的数据可能不同。
    # random_state=0
)

print(x.shape)
print(y.shape)

