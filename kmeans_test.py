# plt.scatter(x[:, 0], x[:, 1])
# plt.show()


# TODO 使用 KMeans 模型进行聚类
# KMeans 是一种无监督聚类算法，它不需要真实标签 y，只根据样本特征 x 自动分组。
# 它的大致工作过程是：
# 1. 先选择 4 个初始中心点；
# 2. 将每个样本分配给距离最近的中心点；
# 3. 根据每组样本重新计算中心点；
# 4. 重复“分配样本、更新中心”，直到中心基本不再变化。

# 3.创建 KMeans 模型。
# n_clusters=4：要求模型把数据划分成 4 个簇，并最终得到 4 个聚类中心。
# 这里设置为 4，是因为前面 make_blobs 人工生成了 4 簇数据。
# 在真实任务中通常并不知道最佳簇数，需要结合业务需求、肘部法或轮廓系数等方法选择。
# 注意：这里只是创建并配置模型，此时模型还没有读取 x，也没有开始聚类。
model = KMeans(n_clusters=4)

# fit_predict(x) 会连续完成两个操作：
# 1. fit：只使用特征数据 x 训练模型，寻找 4 个合适的聚类中心；
# 2. predict：根据样本距离哪个聚类中心最近，为每个样本分配一个簇编号。
#
# y_pred 保存模型给出的聚类结果，形状为 (1000,)，每个元素通常为 0、1、2、3。
# 它等价于先执行 model.fit(x)，再读取 model.labels_。
# KMeans 是无监督学习，所以这里传入的是 x，而不是前面生成的真实标签 y。
#
# 特别注意：聚类编号只是模型分组时使用的代号，没有固定含义。
# 例如，真实标签 y 中的第 0 簇，可能被模型编号为第 2 簇；这不代表模型聚类错误，
# 判断聚类结果时不能直接要求 y_pred 中的数字必须与 y 完全相同。
y_pred = model.fit_predict(x)

# 打印 1000 个样本各自的预测簇编号。
print(y_pred)

# 4.按照预测簇编号绘制散点图，观察聚类结果。
# x[:, 0]：取所有样本的第 1 个特征，作为横坐标。
# x[:, 1]：取所有样本的第 2 个特征，作为纵坐标。
# c=y_pred：根据预测簇编号设置颜色，同一个簇中的样本会显示为相同颜色。
plt.scatter(x[:, 0], x[:, 1], c=y_pred)
plt.show()
